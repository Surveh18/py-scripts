#User Input.
num1 = float(input("Enter the num1 value: "))
num2 = float(input("Enter the num2 value: "))

#Performing OPerations.
Add = num1 + num2
print(f"\nAddition of {num1} + {num2} = {Add}.\n")

Sub = num1 - num2
print(f"Sub of {num1} - {num2} = {Sub}.\n")

Divide = num1 / num2
print(f"Divide of {num1} / {num2} = {Divide}.\n")

Exponent = num1 ** num2
print(f"Exponent of {num1} ** {num2} = {Exponent}.\n")

Floor_div = num1 // num2
print(f"Floor division of {num1} // {num2} = {Floor_div}.\n")

Multiply = num1 * num2
print(f"Multiply of {num1} * {num2} = {Multiply}.\n")

Remainder = num1 % num2
print(f"Remainder of {num1} % {num2} is {Remainder}.\n")
